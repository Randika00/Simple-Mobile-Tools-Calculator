package com.example.new2.models

class calculator(private val number1:Double,private val number2:Double) {


    fun add():Double{

        return number1+number2


    }


    fun subtract():Double{

        return number1-number2


    }

    fun multiply():Double{

        return number1*number2


    }

    fun division():Double{

        return number1/number2


    }




}